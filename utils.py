def delRepo(repo):
    # get output path
    output = getENV("Output");
    path_output = os.path.join(output, 'Output')
    os.chdir(path_output)
    check = False
    # check if repo exists
    list = os.listdir()
    for i in range(0,len(list)):
        if(list[i] == repo):
            check = True
    if(check == False):
        print(Fore.RED+'Error: ResuMe not found'+Fore.WHITE)
        return;
    # delete this repo from output directory
    for root, dirs, files in os.walk(repo):  
        for dir in dirs:
            os.chmod(path.join(root, dir), stat.S_IRWXU)
        for file in files:
            os.chmod(path.join(root, file), stat.S_IRWXU)
    shutil.rmtree(repo)
    
    # delete this repo from github
    pat = getENV("PAT")
    owner = getENV("owner")
    api_url = 'https://api.github.com/repos/'+owner+'/'+repo
    Headers = {
        "Authorization": "token "+pat,
        "Accept": "application/vnd.github.v3+json"
    }
    requests.delete(api_url, headers=Headers)
    print(Fore.GREEN+'\nResuMe Deleted'+Fore.WHITE)